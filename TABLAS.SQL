CREATE TABLE cliente (
    id_cliente NUMBER GENERATED BY DEFAULT AS IDENTITY,
    documento VARCHAR2(50) NOT NULL,
    primer_apellido VARCHAR2(50) NOT NULL,
    segundo_apellido VARCHAR2(50) NOT NULL,
    primer_apellido VARCHAR2(50) NOT NULL,
	segundo_apellido VARCHAR2(50) NOT NULL,
	tipo_documento NUMBER(50) NOT NULL,
	celular VARCHAR2(50) NOT NULL,
	direccion VARCHAR2(50) NOT NULL,
	correo VARCHAR2(50) NOT NULL,
	presupuesto VARCHAR2(50) NOT NULL,
    PRIMARY KEY(id_cliente)

 );
 
 
 CREATE TABLE vehiculo (
    id_vehiculo NUMBER GENERATED BY DEFAULT AS IDENTITY,
    estado VARCHAR2(1) NOT NULL,
    fecha_resgistro timestamp NOT NULL,
    placa VARCHAR2(10) NOT NULL,
    modelo VARCHAR2(10) NOT NULL,
	color VARCHAR2(10) NOT NULL,
	PRIMARY KEY(id_vehiculo)
 );
 
 CREATE TABLE tipo_documento (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    nombre VARCHAR2(50) NOT NULL,
    estado VARCHAR2(1) NOT NULL,
    fecha_resgistro timestamp NOT NULL,
    PRIMARY KEY(id)
 );
 
 CREATE TABLE mecanico (
    id_mecanico NUMBER GENERATED BY DEFAULT AS IDENTITY,
    documento VARCHAR2(50) NOT NULL,
    primer_apellido VARCHAR2(50) NOT NULL,
    segundo_apellido VARCHAR2(50) NOT NULL,
    primer_apellido VARCHAR2(50) NOT NULL,
	segundo_apellido VARCHAR2(50) NOT NULL,
	tipo_documento NUMBER(50) NOT NULL,
	celular VARCHAR2(50) NOT NULL,
	direccion VARCHAR2(50) NOT NULL,
	correo VARCHAR2(50) NOT NULL,
	presupuesto VARCHAR2(50) NOT NULL,
    PRIMARY KEY(id_mecanico)
 );
 
 CREATE TABLE facturas (
    id_cliente NUMBER GENERATED BY DEFAULT AS IDENTITY,
    id_mecanico NUMBER(50) NOT NULL,,
    id_vehiculo NUMBER(50) NOT NULL,
	id_repuesto NUMBER(50) NOT NULL,
	id_servicio NUMBER(50) NOT NULL,
    observaciones VARCHAR2(250) NOT NULL,
    fotos BLOB NOT NULL,
    valor_total VARCHAR2(50) NOT NULL,
	estado VARCHAR2(1) NOT NULL,
	fecha_resgistro timestamp NOT NULL,
	celular VARCHAR2(10) NOT NULL,
	direccion VARCHAR2(50) NOT NULL,
	correo VARCHAR2(50) NOT NULL,
	presupuesto VARCHAR2(10) NOT NULL,
    PRIMARY KEY(id_mecanico,id_mecanico, id_vehiculo)
	
	CONSTRAINT fk_cliente
    FOREIGN KEY (id_cliente)
    REFERENCES cliente(id_cliente)
	
	CONSTRAINT fk_mecanico
    FOREIGN KEY (id_mecanico)
    REFERENCES mecanico(id_mecanico)
	
	CONSTRAINT fk_respuesto
    FOREIGN KEY (id_repuesto)
    REFERENCES respuesto(id_repuesto)
	
	CONSTRAINT fk_servicio
    FOREIGN KEY (id_servicio)
    REFERENCES servicio(id_servicio)
	
 );
 
 CREATE TABLE servicio (
    id_servicio NUMBER GENERATED BY DEFAULT AS IDENTITY,
    precio VARCHAR2(50) NOT NULL,
    descuento VARCHAR2(50) NOT NULL,
    PRIMARY KEY(id_servicio)
 );
 
 
 CREATE TABLE repuesto (
    id_repuesto NUMBER GENERATED BY DEFAULT AS IDENTITY,
    precio VARCHAR2(50) NOT NULL,
    unidades NUMBER(5) NOT NULL,
    descuento VARCHAR2(50) NOT NULL,
    PRIMARY KEY(id_repuesto)
 );
 
  CREATE TABLE parametricas (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    nombre VARCHAR2(50) NOT NULL,
    valor VARCHAR2(50) NOT NULL,
    PRIMARY KEY(id)
 );